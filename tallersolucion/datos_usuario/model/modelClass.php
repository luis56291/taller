  /**
   * 
   * @param type $user
   * @param type $password
   * @param type $activate
   * @return \PDOException
   */
  static public function putNewUser($user, $password, $activate) {
    try {
      $sql = "INSERT INTO usuario (usuario, password, activado) VALUES ('$user', '" . md5($password) . "', $activate)";
      dataBaseClass::getInstance()->beginTransaction();
      $rsp = dataBaseClass::getInstance()->exec($sql);
      dataBaseClass::getInstance()->commit();

      if ($rsp !== false) {
        $rsp = true;
      } else {
        throw new PDOException("El usuario $user está siendo usado", 2745);
      }

      return $rsp;
    } catch (PDOException $e) {
      return $e;
    }
